
def count_in_list(lst: list, occ) -> int:
    """
    Count the number of occurrences of a specific value in a list.

    Args:
        lst (list): The input list to search through
        occ: The value to count occurrences of

    Returns:
        int: The number of times the value appears in the list
    """
    return lst.count(occ)
